public class Lobo {
    private String nome;
    private int idade;
    private boolean vivo;


public Lobo(String nome, int idade){
        this.nome = nome;
        this.idade = idade;
        vivo = true;
    }


public String getNome() {
    return nome;
}


public void setNome(String nome) {
    this.nome = nome;
}


public int getIdade() {
    return idade;
}


public void setIdade(int idade) {
    this.idade = idade;
}





    
    
}
